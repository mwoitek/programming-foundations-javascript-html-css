function changeColor() {
    var firstDiv = document.getElementById("first_div");
    var secondDiv = document.getElementById("second_div");
    firstDiv.className = "dark_sea_green";
    secondDiv.className = "english_lavender";
}

function changeText() {
    var firstDiv = document.getElementById("first_div");
    var secondDiv = document.getElementById("second_div");
    firstDiv.innerHTML = "Primeiro div";
    secondDiv.innerHTML = "Segundo div";
}
